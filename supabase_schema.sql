# Wine Pairing App — Cloud V18

Cloud browser trial build using Supabase Auth and Supabase database.

## Deploy
Replace the files in the GitHub repo with these files, then redeploy on Vercel.

## Required Vercel environment variables
- SUPABASE_URL
- SUPABASE_ANON_KEY

## Supabase
Run `supabase_schema.sql` in the Supabase SQL editor before using cloud sync.
